#!/usr/bin/env bash

set -euo pipefail

echo "==> Preparing release"

# -----------------------------------------------------------------------------
# Tests
# -----------------------------------------------------------------------------

go test ./...

# -----------------------------------------------------------------------------
# Build
# -----------------------------------------------------------------------------

go build ./...

# -----------------------------------------------------------------------------
# GoReleaser
# -----------------------------------------------------------------------------

if command -v goreleaser >/dev/null 2>&1; then
    goreleaser release --clean
else
    echo "GoReleaser is not installed."
    exit 1
fi