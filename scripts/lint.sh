#!/usr/bin/env bash

set -euo pipefail

echo "==> Running linters"

# -----------------------------------------------------------------------------
# Go Formatting
# -----------------------------------------------------------------------------

gofmt -w .

# -----------------------------------------------------------------------------
# goimports
# -----------------------------------------------------------------------------

if command -v goimports >/dev/null 2>&1; then
    goimports -w .
fi

# -----------------------------------------------------------------------------
# golangci-lint
# -----------------------------------------------------------------------------

if command -v golangci-lint >/dev/null 2>&1; then
    golangci-lint run
fi

# -----------------------------------------------------------------------------
# Frontend
# -----------------------------------------------------------------------------

if [ -f "web/package.json" ]; then
    (
        cd web
        npm run lint
    )
fi

echo
echo "Lint complete."